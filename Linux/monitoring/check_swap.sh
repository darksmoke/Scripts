#!/bin/bash
#
# –°–∫—Ä–∏–ø—Ç –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è —Ñ–∞–π–ª–∞ –ø–æ–¥–∫–∞—á–∫–∏ (SWAP)
# –∏ –æ—Ç–ø—Ä–∞–≤–∫–∏ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –≤ Telegram.
# v.1.1
#

# –°—Ç—Ä–æ–≥–∏–π —Ä–µ–∂–∏–º: –≤—ã—Ö–æ–¥ –ø—Ä–∏ –æ—à–∏–±–∫–µ, –ø—Ä–∏ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–∏ –Ω–µ–æ–±—ä—è–≤–ª–µ–Ω–Ω–æ–π –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π
set -euo pipefail

# --- –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è ---

SCRIPT_DIR=$(dirname "$(readlink -f "$0")")

source "${SCRIPT_DIR}/config.ini"
source "${SCRIPT_DIR}/secrets.ini"

# –ó–∞–≥—Ä—É–∂–∞–µ–º –±–∏–±–ª–∏–æ—Ç–µ–∫—É –æ—Ç–ø—Ä–∞–≤–∫–∏
source "${SCRIPT_DIR}/send_telegram.sh"

# --- –û—Å–Ω–æ–≤–Ω–∞—è –ª–æ–≥–∏–∫–∞ ---

echo "–ù–∞—á–∞–ª–æ –ø—Ä–æ–≤–µ—Ä–∫–∏ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è SWAP..."

# –ü–æ–ª—É—á–∞–µ–º –¥–∞–Ω–Ω—ã–µ –ø–æ swap –≤ –º–µ–≥–∞–±–∞–π—Ç–∞—Ö.
# –ü–∞—Ä—Å–∏–º —Å—Ç—Ä–æ–∫—É 'Swap:', –∏–∑–≤–ª–µ–∫–∞—è –æ–±—â–∏–π –æ–±—ä–µ–º (total) –∏ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–Ω—ã–π (used).
read -r TOTAL_MB USED_MB <<< $(free -m | awk '/^Swap:/ {print $2, $3}')

# –ü—Ä–æ–≤–µ—Ä—è–µ–º, —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ª–∏ SWAP –≤ —Å–∏—Å—Ç–µ–º–µ. –ï—Å–ª–∏ total = 0, —Ç–æ –µ–≥–æ –Ω–µ—Ç.
if (( TOTAL_MB == 0 )); then
    echo "INFO: SWAP –Ω–µ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –≤ —Å–∏—Å—Ç–µ–º–µ. –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–µ –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è."
    exit 0
fi

# –†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ–º –ø—Ä–æ—Ü–µ–Ω—Ç –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è
PERCENT_USED=$(( 100 * USED_MB / TOTAL_MB ))

echo "–î–∞–Ω–Ω—ã–µ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏:"
echo "  - –í—Å–µ–≥–æ SWAP: ${TOTAL_MB}MB"
echo "  - –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–æ SWAP: ${USED_MB}MB (${PERCENT_USED}%)"
echo "  - –ü–æ—Ä–æ–≥ —Å—Ä–∞–±–∞—Ç—ã–≤–∞–Ω–∏—è: ${SWAP_USAGE_THRESHOLD_PERCENT}%"

# –°—Ä–∞–≤–Ω–∏–≤–∞–µ–º –ø—Ä–æ—Ü–µ–Ω—Ç –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è —Å –ø–æ—Ä–æ–≥–æ–º
if (( PERCENT_USED > SWAP_USAGE_THRESHOLD_PERCENT )); then
  # –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ –ø—Ä–µ–≤—ã—à–∞–µ—Ç –ø–æ—Ä–æ–≥, —Ñ–æ—Ä–º–∏—Ä—É–µ–º –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ
  HOST=$(hostname)
  TIME=$(date '+%Y-%m-%d %H:%M:%S')

  MSG=$(cat <<EOF
üîÇ *–í—ã—Å–æ–∫–æ–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ SWAP –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ: ${HOST}* üîÇ

üïí *–í—Ä–µ–º—è:* ${TIME}
üìà *–ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–æ SWAP:* ${PERCENT_USED}% (${USED_MB}MB –∏–∑ ${TOTAL_MB}MB)
üìä *–£—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω—ã–π –ø–æ—Ä–æ–≥:* ${SWAP_USAGE_THRESHOLD_PERCENT}%

–ê–∫—Ç–∏–≤–Ω–æ–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ SWAP –º–æ–∂–µ—Ç —Å–≤–∏–¥–µ—Ç–µ–ª—å—Å—Ç–≤–æ–≤–∞—Ç—å –æ –Ω–µ—Ö–≤–∞—Ç–∫–µ –æ–ø–µ—Ä–∞—Ç–∏–≤–Ω–æ–π –ø–∞–º—è—Ç–∏ –∏ –∑–∞–º–µ–¥–ª–µ–Ω–∏–∏ —Ä–∞–±–æ—Ç—ã —Å–∏—Å—Ç–µ–º—ã.
EOF
)

  echo "!!! –í–´–°–û–ö–û–ï –ò–°–ü–û–õ–¨–ó–û–í–ê–ù–ò–ï SWAP! –û—Ç–ø—Ä–∞–≤–∫–∞ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –≤ Telegram..."
  send_telegram "$MSG"
  echo "–£–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ."

else
  # –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ –≤ –Ω–æ—Ä–º–µ
  echo "–£—Ä–æ–≤–µ–Ω—å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è SWAP –≤ –Ω–æ—Ä–º–µ."
fi

exit 0
