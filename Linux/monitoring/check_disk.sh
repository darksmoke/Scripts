#!/bin/bash
#
# –°–∫—Ä–∏–ø—Ç –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ —Å–≤–æ–±–æ–¥–Ω–æ–≥–æ –º–µ—Å—Ç–∞ –Ω–∞ –¥–∏—Å–∫–∞—Ö –∏ –æ—Ç–ø—Ä–∞–≤–∫–∏ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –≤ Telegram.
# v.1.1
#

# –°—Ç—Ä–æ–≥–∏–π —Ä–µ–∂–∏–º: –≤—ã—Ö–æ–¥ –ø—Ä–∏ –æ—à–∏–±–∫–µ, –ø—Ä–∏ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–∏ –Ω–µ–æ–±—ä—è–≤–ª–µ–Ω–Ω–æ–π –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π
set -euo pipefail

# --- –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è ---

SCRIPT_DIR=$(dirname "$(readlink -f "$0")")

source "${SCRIPT_DIR}/config.ini"
source "${SCRIPT_DIR}/secrets.ini"

# –ó–∞–≥—Ä—É–∂–∞–µ–º –±–∏–±–ª–∏–æ—Ç–µ–∫—É –æ—Ç–ø—Ä–∞–≤–∫–∏
source "${SCRIPT_DIR}/send_telegram.sh"

# --- –û—Å–Ω–æ–≤–Ω–∞—è –ª–æ–≥–∏–∫–∞ ---

echo "–ù–∞—á–∞–ª–æ –ø—Ä–æ–≤–µ—Ä–∫–∏ —Å–≤–æ–±–æ–¥–Ω–æ–≥–æ –º–µ—Å—Ç–∞ –Ω–∞ –¥–∏—Å–∫–∞—Ö..."
echo "–ü–æ—Ä–æ–≥ —Å–≤–æ–±–æ–¥–Ω–æ–≥–æ –º–µ—Å—Ç–∞: ${DISK_FREE_SPACE_THRESHOLD}%"
echo "–ò—Å–∫–ª—é—á–µ–Ω–∏—è: ${DISK_EXCLUDE_LIST}"

HOST=$(hostname)

# –ò—Å–ø–æ–ª—å–∑—É–µ–º df —Å –∫–∞—Å—Ç–æ–º–Ω—ã–º –≤—ã–≤–æ–¥–æ–º –¥–ª—è –Ω–∞–¥–µ–∂–Ω–æ—Å—Ç–∏ –∏ –ø—Ä–æ—Å—Ç–æ—Ç—ã –ø–∞—Ä—Å–∏–Ω–≥–∞.
# --output=source,pcent,avail,size,target,fstype
# pcent - –ø—Ä–æ—Ü–µ–Ω—Ç –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è (—Å —Å–∏–º–≤–æ–ª–æ–º %)
# avail - –¥–æ—Å—Ç—É–ø–Ω–æ –≤ –±–∞–π—Ç–∞—Ö
# size - –æ–±—â–∏–π —Ä–∞–∑–º–µ—Ä –≤ –±–∞–π—Ç–∞—Ö
# target - —Ç–æ—á–∫–∞ –º–æ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–∏—è
# fstype - —Ç–∏–ø —Ñ–∞–π–ª–æ–≤–æ–π —Å–∏—Å—Ç–µ–º—ã
# --local - –ø–æ–∫–∞–∑—ã–≤–∞—Ç—å —Ç–æ–ª—å–∫–æ –ª–æ–∫–∞–ª—å–Ω—ã–µ —Ñ–∞–π–ª–æ–≤—ã–µ —Å–∏—Å—Ç–µ–º—ã
df --local --output=pcent,avail,size,target,fstype | tail -n +2 | while read -r line; do
  
  # –†–∞–∑–±–∏—Ä–∞–µ–º —Å—Ç—Ä–æ–∫—É –Ω–∞ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ
  PERCENT_USED_STR=$(echo "$line" | awk '{print $1}')
  AVAIL_KB=$(echo "$line" | awk '{print $2}')
  SIZE_KB=$(echo "$line" | awk '{print $3}')
  MOUNT_POINT=$(echo "$line" | awk '{print $4}')
  FS_TYPE=$(echo "$line" | awk '{print $5}')
  
  # –£–¥–∞–ª—è–µ–º —Å–∏–º–≤–æ–ª '%' –∏–∑ –ø—Ä–æ—Ü–µ–Ω—Ç–æ–≤
  PERCENT_USED=${PERCENT_USED_STR//%}
  
  # --- –ü—Ä–æ–≤–µ—Ä–∫–∞ –∏—Å–∫–ª—é—á–µ–Ω–∏–π ---
  IS_EXCLUDED=false
  for excluded_item in $DISK_EXCLUDE_LIST; do
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ –ø–æ —Ç–∏–ø—É –§–° –∏–ª–∏ –ø–æ —Ç–æ—á–∫–µ –º–æ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–∏—è
    if [[ "$FS_TYPE" == "$excluded_item" ]] || [[ "$MOUNT_POINT" == "$excluded_item" ]]; then
      echo "  - [–ò–°–ö–õ–Æ–ß–ï–ù–û] –†–∞–∑–¥–µ–ª ${MOUNT_POINT} (—Ç–∏–ø: ${FS_TYPE}) –≤ —Å–ø–∏—Å–∫–µ –∏—Å–∫–ª—é—á–µ–Ω–∏–π."
      IS_EXCLUDED=true
      break
    fi
  done
  
  if [[ "$IS_EXCLUDED" == true ]]; then
    continue # –ü–µ—Ä–µ—Ö–æ–¥–∏–º –∫ —Å–ª–µ–¥—É—é—â–µ–º—É —Ä–∞–∑–¥–µ–ª—É
  fi
  # --- –ö–æ–Ω–µ—Ü –ø—Ä–æ–≤–µ—Ä–∫–∏ –∏—Å–∫–ª—é—á–µ–Ω–∏–π ---
  
  # –í—ã—á–∏—Å–ª—è–µ–º –ø—Ä–æ—Ü–µ–Ω—Ç —Å–≤–æ–±–æ–¥–Ω–æ–≥–æ –º–µ—Å—Ç–∞
  PERCENT_FREE=$((100 - PERCENT_USED))
  
  echo "  - –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ä–∞–∑–¥–µ–ª–∞: ${MOUNT_POINT} | –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–æ: ${PERCENT_USED}% | –°–≤–æ–±–æ–¥–Ω–æ: ${PERCENT_FREE}%"

  # –°—Ä–∞–≤–Ω–∏–≤–∞–µ–º –ø—Ä–æ—Ü–µ–Ω—Ç —Å–≤–æ–±–æ–¥–Ω–æ–≥–æ –º–µ—Å—Ç–∞ —Å –ø–æ—Ä–æ–≥–æ–º
  if (( PERCENT_FREE < DISK_FREE_SPACE_THRESHOLD )); then
    
    # –ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ–º –∫–∏–ª–æ–±–∞–π—Ç—ã –≤ —á–µ–ª–æ–≤–µ–∫–æ—á–∏—Ç–∞–µ–º—ã–π —Ñ–æ—Ä–º–∞—Ç –¥–ª—è –∫—Ä–∞—Å–∏–≤–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è
    AVAIL_HUMAN=$(numfmt --to=iec-i --suffix=B --format="%.1f" "$AVAIL_KB")
    SIZE_HUMAN=$(numfmt --to=iec-i --suffix=B --format="%.1f" "$SIZE_KB")
    TIME=$(date '+%Y-%m-%d %H:%M:%S')

    MSG=$(cat <<EOF
üíΩ *–ö—Ä–∏—Ç–∏—á–µ—Å–∫–∏ –º–∞–ª–æ –º–µ—Å—Ç–∞ –Ω–∞ –¥–∏—Å–∫–µ: ${HOST}* üíΩ

üïí *–í—Ä–µ–º—è:* ${TIME}
üíæ *–†–∞–∑–¥–µ–ª:* \`${MOUNT_POINT}\`
üìâ *–°–≤–æ–±–æ–¥–Ω–æ:* ${PERCENT_FREE}% (${AVAIL_HUMAN})
üíø *–û–±—â–∏–π —Ä–∞–∑–º–µ—Ä:* ${SIZE_HUMAN}

–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –æ—Å–≤–æ–±–æ–¥–∏—Ç–µ –º–µ—Å—Ç–æ –Ω–∞ –¥–∏—Å–∫–µ.
EOF
)

    echo "!!! –ü–†–ï–í–´–®–ï–ù–ò–ï –ü–û–†–û–ì–ê –Ω–∞ ${MOUNT_POINT}. –û—Ç–ø—Ä–∞–≤–∫–∞ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –≤ Telegram..."
    send_telegram "$MSG"
    echo "–£–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ."
  fi
done

echo "–ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–∏—Å–∫–æ–≤–æ–≥–æ –ø—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞."
exit 0
