#!/bin/bash
#
# –°–∫—Ä–∏–ø—Ç –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –¥–æ—Å—Ç—É–ø–Ω–æ–π –æ–ø–µ—Ä–∞—Ç–∏–≤–Ω–æ–π –ø–∞–º—è—Ç–∏ (RAM)
# –∏ –æ—Ç–ø—Ä–∞–≤–∫–∏ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –≤ Telegram.
# v.1.0
#

# –°—Ç—Ä–æ–≥–∏–π —Ä–µ–∂–∏–º: –≤—ã—Ö–æ–¥ –ø—Ä–∏ –æ—à–∏–±–∫–µ, –ø—Ä–∏ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–∏ –Ω–µ–æ–±—ä—è–≤–ª–µ–Ω–Ω–æ–π –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π
set -euo pipefail

# --- –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è ---

# –û–ø—Ä–µ–¥–µ–ª—è–µ–º –∞–±—Å–æ–ª—é—Ç–Ω—ã–π –ø—É—Ç—å –∫ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ —Å–æ —Å–∫—Ä–∏–ø—Ç–æ–º
SCRIPT_DIR=$(dirname "$(readlink -f "$0")")
CONFIG_FILE="${SCRIPT_DIR}/config.ini"

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ —Ñ–∞–π–ª–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏
if [[ ! -f "$CONFIG_FILE" ]]; then
  echo "–û–®–ò–ë–ö–ê: –§–∞–π–ª –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ '${CONFIG_FILE}' –Ω–µ –Ω–∞–π–¥–µ–Ω!" >&2
  exit 1
fi

# –ó–∞–≥—Ä—É–∂–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –∏–∑ config.ini –∏ –±–∏–±–ª–∏–æ—Ç–µ–∫—É –æ—Ç–ø—Ä–∞–≤–∫–∏
source "$CONFIG_FILE"
source "${SCRIPT_DIR}/send_telegram.sh"


# --- –û—Å–Ω–æ–≤–Ω–∞—è –ª–æ–≥–∏–∫–∞ ---

echo "–ù–∞—á–∞–ª–æ –ø—Ä–æ–≤–µ—Ä–∫–∏ –¥–æ—Å—Ç—É–ø–Ω–æ–π –æ–ø–µ—Ä–∞—Ç–∏–≤–Ω–æ–π –ø–∞–º—è—Ç–∏..."

# –ò—Å–ø–æ–ª—å–∑—É–µ–º 'free -m' –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –¥–∞–Ω–Ω—ã—Ö –≤ –º–µ–≥–∞–±–∞–π—Ç–∞—Ö.
# –ü–∞—Ä—Å–∏–º —Å—Ç—Ä–æ–∫—É 'Mem:', –∏–∑–≤–ª–µ–∫–∞—è –æ–±—â–∏–π –æ–±—ä–µ–º (total) –∏ –¥–æ—Å—Ç—É–ø–Ω—ã–π (available).
# 'awk' –ø–æ–∑–≤–æ–ª—è–µ—Ç —Å–¥–µ–ª–∞—Ç—å —ç—Ç–æ –≤ –æ–¥–Ω—É —Å—Ç—Ä–æ–∫—É.
read -r TOTAL_MB AVAILABLE_MB <<< $(free -m | awk '/^Mem:/ {print $2, $7}')

# –†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ–º –ø—Ä–æ—Ü–µ–Ω—Ç –¥–æ—Å—Ç—É–ø–Ω–æ–π –ø–∞–º—è—Ç–∏
PERCENT_AVAILABLE=$(( 100 * AVAILABLE_MB / TOTAL_MB ))

echo "–î–∞–Ω–Ω—ã–µ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏:"
echo "  - –í—Å–µ–≥–æ RAM: ${TOTAL_MB}MB"
echo "  - –î–æ—Å—Ç—É–ø–Ω–æ RAM: ${AVAILABLE_MB}MB (${PERCENT_AVAILABLE}%)"
echo "  - –ü–æ—Ä–æ–≥ —Å—Ä–∞–±–∞—Ç—ã–≤–∞–Ω–∏—è: ${RAM_AVAILABLE_THRESHOLD_PERCENT}%"

# –°—Ä–∞–≤–Ω–∏–≤–∞–µ–º –ø—Ä–æ—Ü–µ–Ω—Ç –¥–æ—Å—Ç—É–ø–Ω–æ–π –ø–∞–º—è—Ç–∏ —Å –ø–æ—Ä–æ–≥–æ–º
if (( PERCENT_AVAILABLE < RAM_AVAILABLE_THRESHOLD_PERCENT )); then
  # –ü–∞–º—è—Ç–∏ –º–∞–ª–æ, —Ñ–æ—Ä–º–∏—Ä—É–µ–º –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ
  HOST=$(hostname)
  TIME=$(date '+%Y-%m-%d %H:%M:%S')

  MSG=$(cat <<EOF
üß† *–ö—Ä–∏—Ç–∏—á–µ—Å–∫–∏ –º–∞–ª–æ –æ–ø–µ—Ä–∞—Ç–∏–≤–Ω–æ–π –ø–∞–º—è—Ç–∏ –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ: ${HOST}* üß†

üïí *–í—Ä–µ–º—è:* ${TIME}
üìâ *–î–æ—Å—Ç—É–ø–Ω–æ RAM:* ${PERCENT_AVAILABLE}% (${AVAILABLE_MB}MB)
üíæ *–í—Å–µ–≥–æ RAM:* ${TOTAL_MB}MB
üìä *–£—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω—ã–π –ø–æ—Ä–æ–≥:* ${RAM_AVAILABLE_THRESHOLD_PERCENT}%

–°–∏—Å—Ç–µ–º–µ –º–æ–∂–µ—Ç —Å–∫–æ—Ä–æ –Ω–µ —Ö–≤–∞—Ç–∏—Ç—å –ø–∞–º—è—Ç–∏ –¥–ª—è –Ω–æ–≤—ã—Ö –ø—Ä–æ—Ü–µ—Å—Å–æ–≤.
EOF
)

  echo "!!! –ù–ò–ó–ö–ò–ô –£–†–û–í–ï–ù–¨ –ü–ê–ú–Ø–¢–ò! –û—Ç–ø—Ä–∞–≤–∫–∞ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –≤ Telegram..."
  send_telegram "$MSG"
  echo "–£–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ."

else
  # –ü–∞–º—è—Ç–∏ –¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ
  echo "–£—Ä–æ–≤–µ–Ω—å –¥–æ—Å—Ç—É–ø–Ω–æ–π –ø–∞–º—è—Ç–∏ –≤ –Ω–æ—Ä–º–µ."
fi

exit 0
