#!/bin/bash
#
# –°–∫—Ä–∏–ø—Ç –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ —Å–æ—Å—Ç–æ—è–Ω–∏—è –ø—Ä–æ–≥—Ä–∞–º–º–Ω—ã—Ö RAID-–º–∞—Å—Å–∏–≤–æ–≤ (mdadm)
# –∏ –æ—Ç–ø—Ä–∞–≤–∫–∏ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –≤ Telegram –≤ —Å–ª—É—á–∞–µ –ø—Ä–æ–±–ª–µ–º.
# v.1.0
#

# –°—Ç—Ä–æ–≥–∏–π —Ä–µ–∂–∏–º: –≤—ã—Ö–æ–¥ –ø—Ä–∏ –æ—à–∏–±–∫–µ, –ø—Ä–∏ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–∏ –Ω–µ–æ–±—ä—è–≤–ª–µ–Ω–Ω–æ–π –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π
set -euo pipefail

# --- –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è ---

# –û–ø—Ä–µ–¥–µ–ª—è–µ–º –∞–±—Å–æ–ª—é—Ç–Ω—ã–π –ø—É—Ç—å –∫ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ —Å–æ —Å–∫—Ä–∏–ø—Ç–æ–º
SCRIPT_DIR=$(dirname "$(readlink -f "$0")")
CONFIG_FILE="${SCRIPT_DIR}/config.ini"

# –ü—Ä–æ–≤–µ—Ä—è–µ–º, —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ª–∏ mdadm –≤ —Å–∏—Å—Ç–µ–º–µ. –ï—Å–ª–∏ –Ω–µ—Ç, —Ç–æ –∏ –ø—Ä–æ–≤–µ—Ä—è—Ç—å –Ω–µ—á–µ–≥–æ.
if ! command -v mdadm &> /dev/null; then
    echo "INFO: –ö–æ–º–∞–Ω–¥–∞ 'mdadm' –Ω–µ –Ω–∞–π–¥–µ–Ω–∞. –ü—Ä–æ–≤–µ—Ä–∫–∞ RAID –Ω–µ –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è."
    exit 0
fi

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ —Ñ–∞–π–ª–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏
if [[ ! -f "$CONFIG_FILE" ]]; then
  echo "–û–®–ò–ë–ö–ê: –§–∞–π–ª –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ '${CONFIG_FILE}' –Ω–µ –Ω–∞–π–¥–µ–Ω!" >&2
  exit 1
fi

# –ó–∞–≥—Ä—É–∂–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –∏–∑ config.ini –∏ –±–∏–±–ª–∏–æ—Ç–µ–∫—É –æ—Ç–ø—Ä–∞–≤–∫–∏
source "$CONFIG_FILE"
source "${SCRIPT_DIR}/send_telegram.sh"


# --- –û—Å–Ω–æ–≤–Ω–∞—è –ª–æ–≥–∏–∫–∞ ---

echo "–ù–∞—á–∞–ª–æ –ø—Ä–æ–≤–µ—Ä–∫–∏ —Å–æ—Å—Ç–æ—è–Ω–∏—è –ø—Ä–æ–≥—Ä–∞–º–º–Ω—ã—Ö RAID-–º–∞—Å—Å–∏–≤–æ–≤..."

# –ü–æ–ª—É—á–∞–µ–º —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö –∞–∫—Ç–∏–≤–Ω—ã—Ö md-—É—Å—Ç—Ä–æ–π—Å—Ç–≤
RAID_DEVICES=$(cat /proc/mdstat | grep '^md' | awk '{print "/dev/"$1}')

if [[ -z "$RAID_DEVICES" ]]; then
    echo "INFO: –ê–∫—Ç–∏–≤–Ω—ã–µ RAID-–º–∞—Å—Å–∏–≤—ã –Ω–µ –Ω–∞–π–¥–µ–Ω—ã."
    exit 0
fi

# –§–ª–∞–≥ –¥–ª—è –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏—è –æ–±—â–µ–≥–æ —Å–æ—Å—Ç–æ—è–Ω–∏—è
OVERALL_STATUS_OK=true
# –ü–µ—Ä–µ–º–µ–Ω–Ω–∞—è –¥–ª—è —Å–±–æ—Ä–∞ –ø—Ä–æ–±–ª–µ–º–Ω—ã—Ö –æ—Ç—á–µ—Ç–æ–≤
PROBLEM_REPORTS=""

for device in $RAID_DEVICES; do
    echo "  - –ü—Ä–æ–≤–µ—Ä–∫–∞ —É—Å—Ç—Ä–æ–π—Å—Ç–≤–∞: ${device}"
    
    # –ò—Å–ø–æ–ª—å–∑—É–µ–º 'mdadm --detail' –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è —Å—Ç–∞—Ç—É—Å–∞.
    # –ò—â–µ–º —Å—Ç—Ä–æ–∫–∏, —Å–æ–¥–µ—Ä–∂–∞—â–∏–µ 'State : active, degraded', 'State : active, recovering', –∏ —Ç.–¥.
    # 'State : clean' –∏–ª–∏ 'State : active' - —ç—Ç–æ –Ω–æ—Ä–º–∞.
    # 'grep -v' –∏—Å–∫–ª—é—á–∞–µ—Ç –Ω–æ—Ä–º–∞–ª—å–Ω—ã–µ —Å–æ—Å—Ç–æ—è–Ω–∏—è.
    DEVICE_STATUS=$(mdadm --detail "$device")
    
    # –ò—â–µ–º –ª—é–±–æ–π —Å—Ç–∞—Ç—É—Å, –∫–æ—Ç–æ—Ä—ã–π –Ω–µ —è–≤–ª—è–µ—Ç—Å—è 'clean' –∏–ª–∏ 'active' –±–µ–∑ –¥–æ–ø. —Å–ª–æ–≤.
    # '|| true' –ø—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–∞–µ—Ç –≤—ã—Ö–æ–¥ –ø–æ 'set -e', –µ—Å–ª–∏ grep –Ω–∏—á–µ–≥–æ –Ω–µ –Ω–∞–π–¥–µ—Ç.
    PROBLEMS=$(echo "$DEVICE_STATUS" | grep 'State :' | grep -v -E 'clean|active$') || true

    if [[ -n "$PROBLEMS" ]]; then
        OVERALL_STATUS_OK=false
        echo "!!! –û–ë–ù–ê–†–£–ñ–ï–ù–ê –ü–†–û–ë–õ–ï–ú–ê —Å ${device}!"
        
        # –°–æ–±–∏—Ä–∞–µ–º –ø–æ–ª–Ω—ã–π –æ—Ç—á–µ—Ç –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ –≤ Telegram
        PROBLEM_REPORTS+=$(cat <<EOF
---
–£—Å—Ç—Ä–æ–π—Å—Ç–≤–æ: \`${device}\`
–°–æ—Å—Ç–æ—è–Ω–∏–µ:
\`\`\`
${DEVICE_STATUS}
\`\`\`
---
EOF
)
    else
        echo "    –°—Ç–∞—Ç—É—Å: OK"
    fi
done

# –ï—Å–ª–∏ –±—ã–ª–∏ –æ–±–Ω–∞—Ä—É–∂–µ–Ω—ã –ø—Ä–æ–±–ª–µ–º—ã, –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º –æ–¥–Ω–æ –±–æ–ª—å—à–æ–µ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ
if [[ "$OVERALL_STATUS_OK" == false ]]; then
    HOST=$(hostname)
    TIME=$(date '+%Y-%m-%d %H:%M:%S')

    MSG=$(cat <<EOF
üö® *–û–±–Ω–∞—Ä—É–∂–µ–Ω—ã –ø—Ä–æ–±–ª–µ–º—ã —Å RAID –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ: ${HOST}* üö®

üïí *–í—Ä–µ–º—è:* ${TIME}

–û–±–Ω–∞—Ä—É–∂–µ–Ω—ã –º–∞—Å—Å–∏–≤—ã –≤ —Å–æ—Å—Ç–æ—è–Ω–∏–∏, –æ—Ç–ª–∏—á–Ω–æ–º –æ—Ç 'clean' (–Ω–∞–ø—Ä–∏–º–µ—Ä, degraded, resyncing).
–¢—Ä–µ–±—É–µ—Ç—Å—è –Ω–µ–º–µ–¥–ª–µ–Ω–Ω–æ–µ –≤–º–µ—à–∞—Ç–µ–ª—å—Å—Ç–≤–æ!

*–î–µ—Ç–∞–ª—å–Ω–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è:*
${PROBLEM_REPORTS}
EOF
)

    echo "–û—Ç–ø—Ä–∞–≤–∫–∞ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –æ –ø—Ä–æ–±–ª–µ–º–∞—Ö —Å RAID –≤ Telegram..."
    send_telegram "$MSG"
    echo "–£–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ."
else
    echo "–í—Å–µ RAID-–º–∞—Å—Å–∏–≤—ã –≤ –ø–æ—Ä—è–¥–∫–µ."
fi

exit 0
