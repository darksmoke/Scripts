#!/bin/bash
#
# –°–∫—Ä–∏–ø—Ç –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –≤—Ä–µ–º–µ–Ω–∏ —Ä–∞–±–æ—Ç—ã —Å–∏—Å—Ç–µ–º—ã (uptime) –∏ –æ—Ç–ø—Ä–∞–≤–∫–∏
# —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –≤ —Å–ª—É—á–∞–µ –Ω–µ–¥–∞–≤–Ω–µ–π –ø–µ—Ä–µ–∑–∞–≥—Ä—É–∑–∫–∏.
# v.1.0
#

# –°—Ç—Ä–æ–≥–∏–π —Ä–µ–∂–∏–º: –≤—ã—Ö–æ–¥ –ø—Ä–∏ –æ—à–∏–±–∫–µ, –ø—Ä–∏ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–∏ –Ω–µ–æ–±—ä—è–≤–ª–µ–Ω–Ω–æ–π –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π
set -euo pipefail

# --- –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è ---

# –û–ø—Ä–µ–¥–µ–ª—è–µ–º –∞–±—Å–æ–ª—é—Ç–Ω—ã–π –ø—É—Ç—å –∫ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ —Å–æ —Å–∫—Ä–∏–ø—Ç–æ–º
SCRIPT_DIR=$(dirname "$(readlink -f "$0")")
CONFIG_FILE="${SCRIPT_DIR}/config.ini"

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ —Ñ–∞–π–ª–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏
if [[ ! -f "$CONFIG_FILE" ]]; then
  echo "–û–®–ò–ë–ö–ê: –§–∞–π–ª –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ '${CONFIG_FILE}' –Ω–µ –Ω–∞–π–¥–µ–Ω!" >&2
  exit 1
fi

# –ó–∞–≥—Ä—É–∂–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –∏–∑ config.ini –∏ –±–∏–±–ª–∏–æ—Ç–µ–∫—É –æ—Ç–ø—Ä–∞–≤–∫–∏
source "$CONFIG_FILE"
source "${SCRIPT_DIR}/send_telegram.sh"


# --- –û—Å–Ω–æ–≤–Ω–∞—è –ª–æ–≥–∏–∫–∞ ---

echo "–ù–∞—á–∞–ª–æ –ø—Ä–æ–≤–µ—Ä–∫–∏ –≤—Ä–µ–º–µ–Ω–∏ —Ä–∞–±–æ—Ç—ã —Å–∏—Å—Ç–µ–º—ã..."

# –ü–æ–ª—É—á–∞–µ–º –≤—Ä–µ–º—è —Ä–∞–±–æ—Ç—ã —Å–∏—Å—Ç–µ–º—ã –≤ –º–∏–Ω—É—Ç–∞—Ö
UPTIME_MINUTES=$(awk '{print int($1/60)}' /proc/uptime)

echo "  - –í—Ä–µ–º—è —Ä–∞–±–æ—Ç—ã: ${UPTIME_MINUTES} –º–∏–Ω."
echo "  - –ü–æ—Ä–æ–≥ –¥–ª—è –æ–ø–æ–≤–µ—â–µ–Ω–∏—è: ${UPTIME_ALERT_THRESHOLD_MINUTES} –º–∏–Ω."

# –°—Ä–∞–≤–Ω–∏–≤–∞–µ–º –≤—Ä–µ–º—è —Ä–∞–±–æ—Ç—ã —Å –ø–æ—Ä–æ–≥–æ–º –∏–∑ —Ñ–∞–π–ª–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏
if (( UPTIME_MINUTES < UPTIME_ALERT_THRESHOLD_MINUTES )); then
  HOST=$(hostname)
  TIME=$(date '+%Y-%m-%d %H:%M:%S')

  MSG=$(cat <<EOF
üîÑ *–û–±–Ω–∞—Ä—É–∂–µ–Ω–∞ –Ω–µ–¥–∞–≤–Ω—è—è –ø–µ—Ä–µ–∑–∞–≥—Ä—É–∑–∫–∞ —Å–µ—Ä–≤–µ—Ä–∞: ${HOST}* üîÑ

üïí *–í—Ä–µ–º—è –ø—Ä–æ–≤–µ—Ä–∫–∏:* ${TIME}
‚è±Ô∏è *–í—Ä–µ–º—è —Ä–∞–±–æ—Ç—ã (Uptime):* ${UPTIME_MINUTES} –º–∏–Ω.

–°–µ—Ä–≤–µ—Ä –±—ã–ª –ø–µ—Ä–µ–∑–∞–≥—Ä—É–∂–µ–Ω –º–µ–Ω–µ–µ ${UPTIME_ALERT_THRESHOLD_MINUTES} –º–∏–Ω—É—Ç –Ω–∞–∑–∞–¥.
EOF
)

  echo "!!! –û–ë–ù–ê–†–£–ñ–ï–ù–ê –ù–ï–î–ê–í–ù–Ø–Ø –ü–ï–†–ï–ó–ê–ì–†–£–ó–ö–ê! –û—Ç–ø—Ä–∞–≤–∫–∞ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –≤ Telegram..."
  send_telegram "$MSG"
  echo "–£–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ."
else
  echo "–í—Ä–µ–º—è —Ä–∞–±–æ—Ç—ã —Å–∏—Å—Ç–µ–º—ã –≤ –Ω–æ—Ä–º–µ."
fi

exit 0
